    private void tblmarcaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblmarcaMouseClicked
         try{
        
            int file = tblmarca.getSelectedRow();
            int id = Integer.parseInt(tblmarca.getValueAt(file, 0) .toString());
            
             PreparedStatement ps;
             ResultSet rs;
            
             Connection con = Conexion.getthatConexion();
            ps = con.prepareStatement("SELECT DESCRIPCION FROM MARCA WHERE ID_MARCA=?");

            ps.setInt(1, id);
            rs = ps.executeQuery();

            while(rs.next()){
                txtmarca.setText(String.valueOf(id));
                txtdescripcion.setText(rs.getString("DESCRIPCION"));
                }
            
            } catch(SQLException e){
                   JOptionPane.showMessageDialog(null, e.toString());
            }     
    }//GEN-LAST:event_tblmarcaMouseClicked

